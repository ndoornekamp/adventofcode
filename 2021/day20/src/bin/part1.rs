fn main() {
    let input = include_str!("./input.txt");

    let output = solve(input);
    println!("Part 1 answer: {}", output);
}

fn solve(input: &str) -> usize {
    let mut lines = input.lines();
    let algorithm = lines.next().unwrap();

    lines.next().unwrap();  // empty line

    let mut light_pixels: Vec<(i32, i32)> = vec![];
    for (row_idx, line) in lines.enumerate() {
        for (col_idx, char) in line.char_indices() {
            if char == '#' {
                light_pixels.push((row_idx as i32, col_idx as i32));
            }
        }
    }

    dbg!(algorithm, light_pixels);

    35
}

